import java.util.Scanner;

public class printNumbers
{
    public void print(int n)
    {
        if (n==0)
            return;
        print(n-1);
        System.out.print(n+" ");
    }
    public static void main(String[] args)
    {
        Scanner sc = new Scanner(System.in);
        printNumbers obj = new printNumbers();
        obj.print(sc.nextInt());
    }
}
